#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t len)
{
	char		to_find;
	const char	*mem;

	to_find = (char) c;
	mem = (const char *) str;
	mem--;
	while (len--)
		if (*++mem == to_find)
			return ((void *) mem);
	return (NULL);
}
